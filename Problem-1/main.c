/*

ПРОСТА ЗАДАЧА?
	Програма зчитує двоцифрове число і виводить через пропуск кожну цифрру окремо.

*/

#include <stdio.h>

int main()
{
	int n;
	scanf("%d", &n);

	// Перевіряємо чи знаходиться натуральне число на проміжку від 10 до 99 включно.
	if (10 <= n && n <= 99)
		// Виводимо спочатку першу цифру числа і через пропуск другу
		printf("%d %d\n", n / 10, n % 10);

	return 0;
}